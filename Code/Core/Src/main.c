/* USER CODE BEGIN 4 */
static uint32_t AngleToCCR(uint16_t angle)
{
    const uint32_t MIN_PULSE_US = 500;   // 1 ms
    const uint32_t MAX_PULSE_US = 2500;   // 2 ms

    if (angle > 180U) angle = 180U;        // clamp

    return MIN_PULSE_US +
           ((MAX_PULSE_US - MIN_PULSE_US) * angle) / 180U;
}

void Pan_Servo_SetAngle(uint16_t angle)
{
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, AngleToCCR(angle));
}

static uint16_t ADC_Value_To_Angle(uint32_t adc_val)
{
    /* Clamp just in case */
    if (adc_val > 4095U) adc_val = 4095U;

    /* Linear mapping: 0 → 0°, 4095 → 180° */
    return (uint16_t)((adc_val * 180U) / 4095U);
}
