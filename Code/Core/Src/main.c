/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
	while (1)
	{
		uint32_t adc_raw = 0;

		/* ---- 1. Trigger a single conversion -------------------------------- */
		HAL_ADC_Start(&hadc1);                     // start conversion
		if (HAL_ADC_PollForConversion(&hadc1, 10) == HAL_OK)   // timeout = 10 ms
		{
			adc_raw = HAL_ADC_GetValue(&hadc1);    // 0 … 4095
		}
		HAL_ADC_Stop(&hadc1);                      // stop (optional, saves power)

		/* ---- 2. Convert ADC → angle --------------------------------------- */
		uint16_t angle = ADC_Value_To_Angle(adc_raw);

		/* ---- 3. Convert angle → CCR and write it --------------------------- */
		uint32_t ccr = AngleToCCR(angle);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, ccr);

		/* ---- 4. Small delay – you can tune this to taste ----------------- */
		osDelay(20);      // 20 ms ≈ one PWM period; feel free to increase
	}
  /* USER CODE END 5 */
}

